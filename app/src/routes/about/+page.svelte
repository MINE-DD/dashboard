			<h1>About MINE-DD and Plan-EO</h1>

			<p>
				Plan-EO (pronounced "plan-
				<em>ei</em>
				-oh") is the Planetary Child Health and Enterics Observatory, an interdisciplinary research initiative
				based at the University of Virginia School of Medicine. Through its research, Plan-EO's objective
				is to provide an evidence base for geographical targeting of life-saving child health interventions,
				such as new vaccines. Plan-EO achieves this by producing, curating, and disseminating spatial
				data mapping the transmission risk of key pathogens and the distribution of their environmental
				and sociodemographic determinants in low- and middle-income countries.
			</p>

			<p>MINE-DD is a project that contributes to the Plan-EO initiative.</p>

			<h2>
				MINE-DD: Mining the past to protect the future Diarrheal disease burden in a
				climate-uncertain future
			</h2>

			<p>
				Climate change threatens human health and our planet's health. Diarrheal disease is the
				number two killer of children across the globe. Unchecked, climate change and its impacts on
				our planet will likely reverse hard-won gains in battling diarrheal diseases. Policy makers
				need to understand how diarrheal disease burden will change and how we can better protect
				children in a climate-uncertain future. We propose using artificial intelligence to mine
				existing literature to describe how climate might change diarrheal diseases in the future.
				We then will use this knowledge to project the risk of diarrheal disease into the future,
				preparing communities and informing policy makers.
			</p>

			<p>
